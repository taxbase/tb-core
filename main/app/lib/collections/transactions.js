Transactions = new Mongo.Collection('transactions');
Partitioner.partitionCollection(Transactions);
Transactions.attachSchema(Core.Schemas.Transaction);


if (Meteor.isServer) {
  Transactions.allow({
    insert: function (userId, doc) {
      return false;
    },

    update: function (userId, doc, fieldNames, modifier) {
      return false;
    },

    remove: function (userId, doc) {
      return false;
    }
  });

  Transactions.deny({
    insert: function (userId, doc) {
      return true;
    },

    update: function (userId, doc, fieldNames, modifier) {
      return true;
    },

    remove: function (userId, doc) {
      return true;
    }
  });
}
